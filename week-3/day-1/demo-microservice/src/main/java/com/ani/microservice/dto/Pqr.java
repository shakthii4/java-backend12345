package com.ani.microservice.dto;

import lombok.Data;

import java.time.LocalDate;
@Data

public class Pqr
{
    private Long id;
    private String name;
    private LocalDate date;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public LocalDate getDate() {
        return date;
    }

    public void setDate(LocalDate date) {
        this.date = date;
    }
}
